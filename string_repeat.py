def repeat_str(repeat, string):
    return repeat * string

# This solution multiplies the string by the number of times indicated to repeat the string

print(repeat_str(4, 'a'))
print(repeat_str(3, 'hello '))
print(repeat_str(2, 'abc'))
print(repeat_str(0, ''))
print(repeat_str(0, 'I'))
print(repeat_str(5, ''))
print(repeat_str(6, 'I'))
print(repeat_str(5, 'Hello'))